void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim){
	if (htim == &htim4){
		if (HAL_GPIO_ReadPin(R0) == GPIO_PIN_SET){
			keypress[4*col_index]=0;
		}else{
			keypress[4*col_index]++;
		}
		if (HAL_GPIO_ReadPin(R1) == GPIO_PIN_SET){
			keypress[4*col_index+1]=0;
		}else{
			keypress[4*col_index+1]++;
		}
		if (HAL_GPIO_ReadPin(R2) == GPIO_PIN_SET){
			keypress[4*col_index+2]=0;
		}else{
			keypress[4*col_index+2]++;
		}
		if (HAL_GPIO_ReadPin(R3) == GPIO_PIN_SET){
			keypress[4*col_index+3]=0;
		}else{
			keypress[4*col_index+3]++;
		}
		col_index+=1;
		if (col_index>3){
			col_index=0;
		}
		HAL_GPIO_WritePin(C0, col_index==0 ? GPIO_PIN_SET:GPIO_PIN_RESET);
		HAL_GPIO_WritePin(C1, col_index==1 ? GPIO_PIN_SET:GPIO_PIN_RESET);
		HAL_GPIO_WritePin(C2, col_index==2 ? GPIO_PIN_SET:GPIO_PIN_RESET);
		HAL_GPIO_WritePin(C3, col_index==3 ? GPIO_PIN_SET:GPIO_PIN_RESET);
	}

	else if (htim == &htim3){
		HAL_TIM_PWM_Stop(&htim2, TIM_CHANNEL_3); // if bit=1 not
		if (i < 9){
			uint8_t bit_i_value = (byte >> i) & 1;
			if (bit_i_value == 0){
				HAL_TIM_PWM_Start(&htim2, TIM_CHANNEL_3); //if bit=0 pwm
			}
		}
		else if (i==10){
			HAL_TIM_Base_Stop_IT(&htim3);
			byte_sent = 1;
		}
		i++;
	}

	else if (htim == &htim5){
	    spi_data[0] = rows[leds][col_index2]; // light the leds acording to the data recieved (leds)
	    spi_data[1] = 1 << col_index2;

	    HAL_SPI_Transmit_DMA(&hspi1, spi_data, 2); //Transmit the data
	  }

}


void HAL_SPI_TxCpltCallback(SPI_HandleTypeDef *hspi){ //Transmission ended, pulse the rclk and move to next column
	HAL_GPIO_WritePin(GPIOB,GPIO_PIN_6, GPIO_PIN_SET);
	if (++col_index2 > 4){
		col_index2 = 0;
	}
	HAL_GPIO_WritePin(GPIOB,GPIO_PIN_6, GPIO_PIN_RESET);
}


void send_byte(){
	__HAL_TIM_CLEAR_IT(&htim3,TIM_IT_UPDATE);
	HAL_TIM_PWM_Start(&htim2, TIM_CHANNEL_3); //Start bit
	HAL_TIM_Base_Start_IT(&htim3); //Start baud rate timer
	byte_sent=0;
	i = 0;
}


void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart) { //When data is received store it

	if (huart == &huart1){
		leds = data;
	}
	HAL_UART_Receive_IT(&huart1, &data, 1); //Can start new receiving
}


