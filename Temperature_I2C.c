const uint16_t LM75_address_write = 0b10010000;
const uint16_t LM75_address_read = 0b10010001;
const uint8_t LM75_temp = 0x00;


void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim) {
	if (htim == &htim2) {
		uint8_t buf[6];
		int16_t temp_int;
		float temp_converted;
		char string[32];

		if (HAL_I2C_Master_Receive(&hi2c1, LM75_address_read, &buf,  6,  20) == HAL_OK){
			 //testing negative temperatures, should return -54.875
			 /*uint8_t a = 0b11001001;
			 uint8_t b = 0b00100000;
			 temp_int = ((a << 8) + b);*/

			 if (buf[0]==buf[2] && buf[1]==buf[3]){
				 temp_int = (buf[0]<<8) + buf[1];
			 } else{
				 temp_int = (buf[4]<<8) + buf[5];
			 }

			 temp_int = temp_int >> (16 - ADC_BITS); //sign-extended right shift for signed variable

			 temp_converted = temp_int * RESOLUTION;
			 snprintf(string,sizeof(string),"Temperature: %.3f ÂºC\r\n", temp_converted);
		} else {
			snprintf(string,sizeof(string),"Error\r\n");
		}
		HAL_UART_Transmit(&huart2,(uint8_t*)string,strlen(string),200);
	}
}


